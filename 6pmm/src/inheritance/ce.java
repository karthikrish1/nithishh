public class ce {

	public static void main(String[] args) {
		
		ce.main(4);
		ce.main(3.4);
		
		
	}
	
	public static void main( int s)
	{
		System.out.println("Area of square is "+ s*s);
	}
	
	
	public static void main(double r)
	{
		System.out.println("Area of circle is "+ 3.14*r*r);
	}
}
