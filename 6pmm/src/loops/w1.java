package loops;

public class w1 {
public static void main(String[] args) {
	/*
	 * for/while/do while
	 * 
	 * initialisation:  where to start 
	 * 
	 * condition:  where to stop
	 *             frwed: <= endvalue
	 *             backward: >=end value
	 *             
	 * inc/dec   : diff between 2 numbers 
	 * 
	 * while loop
	 * 
	 * initialisation
	 * while(condition)
	 * {
	 * statement
	 * inc/dec
	 * }
	 *  * odd numbers from 3 5 7 9 11
	 *  even numbers from 90 to 80
	 * */
	int i=90;
	while(i>=80)
	{
		System.out.println(i);
		i=i-2;
	}
}
}
